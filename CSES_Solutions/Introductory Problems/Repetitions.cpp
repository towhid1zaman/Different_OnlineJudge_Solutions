void task(){
    string s; cin >> s;
    int ans = 0;
    int n = s.size();
    for(int i = 0; i<n;){
        int cnt = 0;
        for(int j = i; j<n; j++){
            if(s[i]==s[j])cnt++;
            else break;
        }
        ans = max(ans, cnt);
        i+=cnt;
    }
    cout << ans << endl;
}

