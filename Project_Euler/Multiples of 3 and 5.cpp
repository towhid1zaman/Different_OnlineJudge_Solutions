/*
Problem statement: Find the sum of all the multiples of 3 or 5 below 1000.
*/

void task(){
    ll ans = 0;
    for(int i = 0; i<1000; i++){
        if(i%3==0 or i%5==0){
            ans+=i;
        }
    }
    cout << ans << endl;
}

